package Classes;
import java.util.ArrayList;

public class Student {
	private String name;
	private int numGroup;
	private int numKursa;
	
	// Список оценок
	public ArrayList<Integer> marks = new ArrayList<Integer>();
	
	public Student(String name, int numGroup, int numKursa, ArrayList<Integer> marks)
	{
		this.setName(name);
		this.setNumGroup(numGroup);
		this.setNumKursa(numKursa);
		this.marks = marks;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNumKursa() {
		return numKursa;
	}
	public void setNumKursa(int numKursa) {
		this.numKursa = numKursa;
	}
	public int getNumGroup() {
		return numGroup;
	}
	public void setNumGroup(int numGroup) {
		this.numGroup = numGroup;
	}
}
